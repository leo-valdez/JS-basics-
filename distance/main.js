var canvas=document.querySelector("canvas");
canvas.height=400;
canvas.width=400;
x1=110;
y1=170;
r1=21;
x2=200;
y2=300;
r2=17;
var c=canvas.getContext("2d");
c.beginPath();
c.fillStyle="pink";
c.arc(x1,y1,r1,Math.PI*2,false);
c.fill();
c.beginPath();
c.fillStyle="grey";
c.arc(x2,y2,r2,Math.PI*2,false);
c.fill();
dis=Math.sqrt(Math.pow((x2-x1),2)+Math.pow((y2-y1),2));
c.moveTo(0,25);
c.lineTo(400,25);
c.stroke();
c.font="20px Georgia";
c.strokeStyle="black";
c.strokeText(dis+"/",10,15);